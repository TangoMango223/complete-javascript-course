// Let's look at Math Operators

// Example #1 - Christine's age in 2024
const ageChristine = 2037 - 1997;
console.log(ageChristine);


// Age of Sarah - she's old haha!
const ageSarah = 2037 - 2020;
console.log(ageSarah);

// Multiple things to do:
console.log(ageChristine, ageSarah);

// By default we are using Constants or CONST

// We can log multiple values at once.
console.log(ageChristine * 2, (ageChristine / 10) ** 2)

// Similar to Python, 2 ** 3  = 8 
// PEDMAS also applies here


// Example with strings
const firstName = "Christine"
const lastName = "Tang"

// Full name
console.log(firstName + lastName);

// Add a space to separate it out, yay!
console.log(firstName + " " + lastName);

// Template strings, like F-String, for better ways of doing this in the future

// Type of operator:
console.log(typeof firstName);

// Assignment Operator
// Use equal sign
// Will equal 15
let x = 10 + 5;


// Console log
console.log(x);

// Assignment Operators: 
x += 10 // adds 10, so 25
x *= 4 // 4 x 25 = 100
x++ // adds 1 -> 100 + 1
x--; // 101 - 1 = 100


console.log(x);

// Comparison Operators - Christine is older than Sarah
console.log(ageChristine > ageSarah);

// There are multiple, like < , > , <= and >=
console.log(ageSarah >= 18);
// Is she an adult? She's 17 by 2037 lol
